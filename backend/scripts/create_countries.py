def run():
    """
    Django management command to create countries from the all_countries list.
    Run with: python manage.py shell < scripts/create_countries.py
    Or from within the shell: exec(open('scripts/create_countries.py').read())
    """
    from api.models import Country
    
    created_count = 0
    updated_count = 0
    error_count = 0
    
    print("Starting country import...")
    print(f"Total countries to process: {len(all_countries)}\n")
    
    for country_data in all_countries:
        try:
            # Extract only the fields that the Country model needs
            country, created = Country.objects.get_or_create(
                iso_code=country_data['iso_code'],
                defaults={
                    'name': country_data['name'],
                    'flag_emoji': country_data.get('flag_emoji', ''),
                    'continent': country_data.get('continent', ''),
                }
            )
            
            if created:
                created_count += 1
                print(f"✓ Created: {country.name} ({country.iso_code})")
            else:
                # Update name and other fields in case they changed
                updated = False
                if country.name != country_data['name']:
                    country.name = country_data['name']
                    updated = True
                if country.flag_emoji != country_data.get('flag_emoji', ''):
                    country.flag_emoji = country_data.get('flag_emoji', '')
                    updated = True
                if country.continent != country_data.get('continent', ''):
                    country.continent = country_data.get('continent', '')
                    updated = True
                
                if updated:
                    country.save()
                    updated_count += 1
                    print(f"↻ Updated: {country.name} ({country.iso_code})")
                else:
                    print(f"→ Skipped: {country.name} ({country.iso_code}) (no changes)")
                    
        except Exception as e:
            error_count += 1
            print(f"✗ Error creating {country_data['name']}: {str(e)}")
    
    print(f"\n{'='*50}")
    print(f"Import Complete!")
    print(f"Created: {created_count}")
    print(f"Updated: {updated_count}")
    print(f"Errors: {error_count}")
    print(f"Total processed: {created_count + updated_count + (len(all_countries) - created_count - updated_count - error_count)}")
    print(f"{'='*50}")


# Run the import if this script is executed
if __name__ == '__main__':
    run()


